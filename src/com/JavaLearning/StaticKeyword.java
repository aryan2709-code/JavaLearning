package com.JavaLearning;

public class StaticKeyword {
    // The 'static' keyword is mainly used for efficient memory management.
    // It can be applied to variables, methods, blocks, and nested classes.
    // Static members belong to the class itself, not to any specific object.
    // Static methods cannot access non-static members or invoke non-static methods directly.
    // 'this' and 'super' cannot be used inside a static context.

    // Static blocks are executed once when the class is loaded into memory.
    // They are typically used for initializing static variables or performing one-time setup.
}
